    public void limpiar() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo = (DefaultTableModel) this.tb_Productos.getModel();

        for (int k = 0; k <= modelo.getRowCount(); k++) {
            modelo.removeRow(0);
        }
    }

    public Producto buscar(String nombre) {
        Producto aux = null;
        for (int k = 0; k <= this.ListaProducto.size(); k++) {
            if (this.ListaProducto.get(k).getNombre().equals(nombre)) {
                aux = this.ListaProducto.get(k);
                i = k;
                break;
            }
        }
        return aux;
    }


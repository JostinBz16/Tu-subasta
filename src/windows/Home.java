    private void btn_filMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_filMouseClicked

        String tipo = cmb_categorias.getSelectedItem().toString();

        ArrayList<Producto> filtrado = new ArrayList<>();
        for (int j = 0; j < ListaProducto.size(); j++) {
            if (ListaProducto.get(j).getCategoria().equals(tipo)) {
                filtrado.add(ListaProducto.get(j));
            }
        }
        DefaultTableModel modelo = new DefaultTableModel();

        modelo = (DefaultTableModel) tb_Productos.getModel();
        modelo.setNumRows(filtrado.size());
        for (int j = 0; j < filtrado.size(); j++) {

            modelo.setValueAt(filtrado.get(j).getNombre(), j, 0);
            modelo.setValueAt(filtrado.get(j).getEstado(), j, 1);
            modelo.setValueAt(filtrado.get(j).getCategoria(), j, 2);
            modelo.setValueAt(filtrado.get(j).getFechaFinal(), j, 3);
            JLabel jl = new JLabel();
            jl.setIcon(new ImageIcon(filtrado.get(j).getImagen()));
            modelo.setValueAt(jl, j, 5);
        }
    }//GEN-LAST:event_btn_filMouseClicked


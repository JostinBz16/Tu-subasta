    private void btn_ofertMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_ofertMouseClicked
        int i = this.tb_Productos.getSelectedRow();
        if (i < 0) {
            JOptionPane.showMessageDialog(this, "Debe selecionar un Producto", "Advertencia", JOptionPane.WARNING_MESSAGE);
        } else {
            if (ListaProducto.get(i).getNombre().equals(tb_Productos.getModel().getValueAt(i, 0).toString())) {
                JOptionPane.showMessageDialog(this, "Ofrece un cantidad mayor al postor que esta actualmente para asi ser el tu el nuevo y tengas mas oportunidad de ganar", "Advertencia", JOptionPane.WARNING_MESSAGE);
                n_pro.setText(ListaProducto.get(i).getNombre());
                descripcion.setText(ListaProducto.get(i).getDescripcion());
                CategoriaProducto.setText("Categoria : " + ListaProducto.get(i).getCategoria());
                lbl_fechaInicio.setText(ListaProducto.get(i).getFechaInificio().toString());
                lbl_fechaFinal.setText(ListaProducto.get(i).getFechaFinal().toString());
                Lbl_OfertaMinima.setText(" $ " + String.valueOf(ListaProducto.get(i).getValorMininimo()));
                EstadoProducto.setText("Estado : " + ListaProducto.get(i).getEstado());
            }
        }

    }//GEN-LAST:event_btn_ofertMouseClicked


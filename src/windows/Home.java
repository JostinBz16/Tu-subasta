    private void ofertarMontoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ofertarMontoMouseClicked
        Subasta subastas = new Subasta();
        for (int j = 0; j < ListaSubasta.size(); j++) {
            String lista = ListaSubasta.get(j).getProducto().getNombre();
            if (ListaSubasta.contains(ListaSubasta.get(j).equals(lista))) {
                subasta.setProducto(producto);
                subastas.setNombrePostor(usuario.getNombre());
                subastas.setMonto(Float.parseFloat(this.TxtOferta.getText()));

                JOptionPane.showMessageDialog(null, "Eres el nuevo mayor postor de la subasta de este producto");
                this.TxtOferta.setText("");
                this.ListaSubasta.set(j, subasta);
                this.almcenardatosSubasta();
            } else {
                subasta.setProducto(producto);
                subastas.setNombrePostor(usuario.getNombre());
                subastas.setMonto(Float.parseFloat(this.TxtOferta.getText()));

                JOptionPane.showMessageDialog(null, "Eres el nuevo mayor postor de la subasta de este producto");
                this.TxtOferta.setText("");
                this.ListaSubasta.add(subasta);
                this.almcenardatosSubasta();
            }
        }

    }//GEN-LAST:event_ofertarMontoMouseClicked


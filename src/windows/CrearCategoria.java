    private void btn_agregarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_agregarMouseClicked

        if (cateNombre.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Error coloque un nombre a la categoria");
        } else {
            Categorias categoria = new Categorias();
            categoria.setNombre(this.cateNombre.getText());

            this.cateNombre.setText("");

            this.ListaCategoria.add(categoria);
            this.almcenardatos();
        }
        limpiarFormulario();
        this.mostrar();
    }//GEN-LAST:event_btn_agregarMouseClicked

    private void btn_deleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_deleteMouseClicked
        
        Categorias categoria = new Categorias();
        categoria.setNombre(this.cateNombre.getText());
        
        categoria.setNombre(this.cateNombre.getText());
        this.txt_buscar.setText("");
        JOptionPane.showMessageDialog(null, "Â¿Desea eliminar la categoria?");
        this.ListaCategoria.remove(i);
        
        this.limpiar();
        limpiarFormulario();
        this.almcenardatos();
        this.mostrar();
        this.Desabilitar();
        
    }//GEN-LAST:event_btn_deleteMouseClicked

    private void btn_modificarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_modificarMouseClicked
        
        Categorias categoria = new Categorias();
        categoria.setNombre(this.cateNombre.getText());
        
        this.ListaCategoria.set(i, categoria);
        this.almcenardatos();
        
        this.mostrar();
        limpiarFormulario();
        this.Desabilitar();
    }//GEN-LAST:event_btn_modificarMouseClicked

    private void tablaCateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaCateMouseClicked
        
        i = this.tablaCate.getSelectedRow();
        this.cateNombre.setText(this.tablaCate.getModel().getValueAt(i,0).toString());
        
        habilitar();
        
    }//GEN-LAST:event_tablaCateMouseClicked

    public void almcenardatos() {
        try {
            ObjectOutputStream salida = new ObjectOutputStream(new FileOutputStream("categorias.dat"));

            salida.writeObject(this.ListaCategoria);
            JOptionPane.showMessageDialog(null, "Se ha registrado con exito");
        } catch (IOException e) {
            System.out.println("Error al escribir en el archivo " + e.getMessage());
        }
    }

    public void extraerdatos() {

        try {
            ObjectInputStream entrada = new ObjectInputStream(new FileInputStream("categorias.dat"));
            this.ListaCategoria = (ArrayList<Categorias>) entrada.readObject();

            entrada.close();
        } catch (Exception e) {
            System.out.println("Error al escribir en el archivo " + e.getMessage());
        }
    }

    public void limpiar() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo = (DefaultTableModel) this.tablaCate.getModel();

        for (int k = 0; k <= modelo.getRowCount(); k++) {
            modelo.removeRow(0);
        }
    }

    public Categorias buscar(String clave) {
        Categorias aux = null;
        for (int k = 0; k <= this.ListaCategoria.size(); k++) {
            if (this.ListaCategoria.get(k).getNombre().equals(clave)) {
                aux = this.ListaCategoria.get(k);
                i = k;
                break;
            }
        }

        return aux;
    }

    public void mostrar() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo = (DefaultTableModel) this.tablaCate.getModel();
        modelo.setNumRows(this.ListaCategoria.size());
        for (int i = 0; i < this.ListaCategoria.size(); i++) {
            modelo.setValueAt(this.ListaCategoria.get(i).getNombre(), i, 0);
        }
    }
    
